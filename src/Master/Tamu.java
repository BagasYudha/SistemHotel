    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed

        int selectedRow = tblTamu.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String idTamu = tblTamu.getValueAt(selectedRow, 0).toString(); // Assuming id_barang is in the first column

        int option = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (option == JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM tb_tamu WHERE id_tamu = ?";

                java.sql.Connection conn = koneksi.ConnectionDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, idTamu);
                int rowsDeleted = pst.executeUpdate();

                if (rowsDeleted > 0) {
                    refreshTable();
                    buka_tabel();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete record.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error deleting record: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
         try {
            java.sql.Connection conn = koneksi.ConnectionDB();

            String sql = "UPDATE tb_tamu SET "
            + "nama = ?, "
            + "alamat = ?, "
            + "kontak = ? "
            + "WHERE id_tamu = ?";

            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, txtNama.getText());
            pst.setString(2, txtAlamat.getText());
            pst.setString(3, txtKontak.getText());
            pst.setInt(4, Integer.parseInt(txtIdTamu.getText()));

            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil di Edit");

            txtIdTamu.setText("");
            txtNama.setText("");
            txtAlamat.setText("");
            txtKontak.setText("");
            txtIdTamu.setEnabled(true);

            txtIdTamu.requestFocusInWindow();

            buka_tabel();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Proses edit gagal: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed

        String sql = "INSERT INTO tb_tamu (id_tamu, nama, alamat, kontak) VALUES (?, ?, ?, ?)";
        try {
            java.sql.Connection conn = koneksi.ConnectionDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, txtIdTamu.getText());
            pst.setString(2, txtNama.getText());
            pst.setString(3, txtAlamat.getText());
            pst.setString(4, txtKontak.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data inserted successfully!");

            clearFields();

            refreshTable();

            buka_tabel();

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error inserting data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnSubmitActionPerformed

