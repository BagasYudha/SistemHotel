    private void refreshTable() {
    txtIdTamu.setText("");
    txtNama.setText("");
    txtAlamat.setText("");
    txtKontak.setText("");
    }

    private void clearFields() {
    DefaultTableModel model = (DefaultTableModel) tblTamu.getModel();
    model.setRowCount(0); // Clear the existing rows
    buka_tabel(); // Reload the data
    }

    private void buka_tabel() {
        
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("Id");
    model.addColumn("Nama");
    model.addColumn("Alamat");
    model.addColumn("Kontak");
    try {
        String sql = "select * from tb_tamu";
        java.sql.Connection conn = koneksi.ConnectionDB();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);
        while(res.next()){
            model.addRow(new Object[]{res.getString(1), res.getString(2),
                res.getString(3), res.getString(4)});   
        }
        tblTamu.setModel(model);
    } catch (Exception e){
        e.printStackTrace();
    }
    }
